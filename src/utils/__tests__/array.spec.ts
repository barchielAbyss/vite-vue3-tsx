import { fullArray } from '../Array'

describe('Array', () => {
  it('fullArray', () => {
    expect(fullArray([1, 2])).toEqual({
      array: [[1], [2], [1, 2]],
      total: 3,
    })

    expect(fullArray([1, 2, 3])).toEqual({
      array: [[1], [2], [1, 2], [3], [1, 3], [2, 3], [1, 2, 3]],
      total: 7,
    })

    expect(fullArray([1, 2, 3, 4, 5])).toEqual({
      array: [
        [1],
        [2],
        [1, 2],
        [3],
        [1, 3],
        [2, 3],
        [1, 2, 3],
        [4],
        [1, 4],
        [2, 4],
        [1, 2, 4],
        [3, 4],
        [1, 3, 4],
        [2, 3, 4],
        [1, 2, 3, 4],
        [5],
        [1, 5],
        [2, 5],
        [1, 2, 5],
        [3, 5],
        [1, 3, 5],
        [2, 3, 5],
        [1, 2, 3, 5],
        [4, 5],
        [1, 4, 5],
        [2, 4, 5],
        [1, 2, 4, 5],
        [3, 4, 5],
        [1, 3, 4, 5],
        [2, 3, 4, 5],
        [1, 2, 3, 4, 5],
      ],
      total: 31, // 2 的 5次方(参数长度) - 1
    })
  })
})
